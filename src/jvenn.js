function elementIndex( el ) {
  var i = -1,
  count = 1,
  childs = el.parentNode.childNodes;

  if (el.parentNode) {
    while( childs[ ++i ] !== el ) {
      if ( childs[ i ].nodeType === 1 ) {
        count++;
      }
    }
    return count;
  } else {
    return -1;
  }
}

